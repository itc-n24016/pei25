a = 10
b = 1
x = []

while b <= 3:
    y = divmod(a, b) # divmodは(a // b, a % b)を返す　1回目は(10 // 1, 10 % 1) = (10 , 0)
    y = list(y) # タプルyをリスト[10, 0]に変換
    x += y # リストxにyの要素を追加　1回目はx = [10, 0]
    b += 1 # bをインクリメント　繰り返しごとに1 -> 2 -> 3
# b 1       2      3 
# x [10, 0] [5, 0] [3, 1]

x = tuple(x) # リストxをタプルに変換
print(x)
